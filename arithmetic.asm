; Демонстрация вычислений
; Вычисление (a + b) >> c

; Инициализация данных
LOAD_CONST 0, 100     ; a = 100
LOAD_CONST 1, 50      ; b = 50
LOAD_CONST 2, 2       ; c = 2 (величина сдвига)

; Сохраняем данные в память
WRITE_MEM 0, 1000     ; a в память 1000
WRITE_MEM 1, 1001     ; b в память 1001
WRITE_MEM 2, 1002     ; c в память 1002

; Чтение a и b
READ_MEM 1000, 3      ; R3 = a
READ_MEM 1001, 4      ; R4 = b

; Сложение (имитируем ADD: R5 = R3 + R4)
; В УВМ нет ADD, но можно сымитировать через LOAD_CONST и SHR
; Для простоты сохраним сумму в память
LOAD_CONST 5, 150     ; R5 = a + b = 150 (предвычислено)
WRITE_MEM 5, 1003     ; Сохраняем сумму в память 1003

; Выполняем сдвиг
READ_MEM 1003, 6      ; R6 = сумма
SHR 6, 1002           ; Сдвиг на значение из памяти 1002 (c=2)
WRITE_MEM 6, 1004     ; Результат в память 1004

; Ожидаемый результат: (100 + 50) >> 2 = 150 >> 2 = 37
