; Поэлементный побитовый сдвиг вправо над двумя векторами длины 6
; Вектор A: исходные данные
; Вектор B: результаты
; Величина сдвига: 2

; Инициализация величины сдвига
LOAD_CONST 10, 2      ; Величина сдвига = 2
WRITE_MEM 10, 500     ; Сохраняем в память по адресу 500

; Инициализация вектора A (6 элементов)
LOAD_CONST 0, 1000    ; Базовый адрес вектора A
LOAD_CONST 1, 2000    ; Базовый адрес вектора B (результат)

; Элемент 0: A[0] = 255 (0b11111111)
LOAD_CONST 2, 255
WRITE_MEM 2, 1000

; Элемент 1: A[1] = 128 (0b10000000)
LOAD_CONST 2, 128
WRITE_MEM 2, 1001

; Элемент 2: A[2] = 64 (0b01000000)
LOAD_CONST 2, 64
WRITE_MEM 2, 1002

; Элемент 3: A[3] = 32 (0b00100000)
LOAD_CONST 2, 32
WRITE_MEM 2, 1003

; Элемент 4: A[4] = 16 (0b00010000)
LOAD_CONST 2, 16
WRITE_MEM 2, 1004

; Элемент 5: A[5] = 8 (0b00001000)
LOAD_CONST 2, 8
WRITE_MEM 2, 1005

; Выполнение сдвига для каждого элемента
; Элемент 0
READ_MEM 1000, 3      ; Чтение A[0]
SHR 3, 500            ; Сдвиг на значение из памяти 500
WRITE_MEM 3, 2000     ; Запись B[0]

; Элемент 1
READ_MEM 1001, 3      ; Чтение A[1]
SHR 3, 500            ; Сдвиг
WRITE_MEM 3, 2001     ; Запись B[1]

; Элемент 2
READ_MEM 1002, 3      ; Чтение A[2]
SHR 3, 500            ; Сдвиг
WRITE_MEM 3, 2002     ; Запись B[2]

; Элемент 3
READ_MEM 1003, 3      ; Чтение A[3]
SHR 3, 500            ; Сдвиг
WRITE_MEM 3, 2003     ; Запись B[3]

; Элемент 4
READ_MEM 1004, 3      ; Чтение A[4]
SHR 3, 500            ; Сдвиг
WRITE_MEM 3, 2004     ; Запись B[4]

; Элемент 5
READ_MEM 1005, 3      ; Чтение A[5]
SHR 3, 500            ; Сдвиг
WRITE_MEM 3, 2005     ; Запись B[5]
